<?php

#三位數的隨機數
for($i=0; $i<10; $i++){
    echo rand(100, 999). '<br>';
}


echo '-----------------<br>';


#七位數的隨機數
for($i=0; $i<10; $i++){
    echo rand(1000000, 9999999). '<br>';
}



echo '-----------------<br>';



#七位數的隨機數(補零)
for($i=0; $i<10; $i++){
    //.07d表示不足7位數時在前面補零
    printf("%'.07d<br>", rand(1, 100000));
}


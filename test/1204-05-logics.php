<?php

#邏輯運算子

#運算子之間的優先順序
# &&和|| > = > and和or

#||和&&優先於=所以印出布林值
$a = 7 || 5;
$b = 0 || 5;

#\為跳脫字元
echo "\$a=$a, \$b=$b<br>";
#var_dump為查看值為何種類型的function，可用來除錯
var_dump($a);
################################################
echo "<br>";
# =優先於or，所以以=為主
$a = 7 or 5;
$b = 0 or 5;

echo "\$a=$a, \$b=$b<br>";
#回傳值的類型為整數
var_dump($a);
echo "<br>";
# =優先於and，所以以=為主
$a = 7 and 5;
$b = 0 and 5;

echo "\$a=$a, \$b=$b<br>";